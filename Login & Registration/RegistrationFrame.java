    /*
    * This event handler method(on MouseClick) reads all the text and password
    * fields and also validates each field. If it is valid the data is inserted
    * into the database else a message dialog is displayed.
     */
    private void signUpButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signUpButtonMouseClicked
        String userName = userNameField.getText();
        String fullName = fullNameField.getText();
        String email = emailField.getText();
        String password = String.valueOf(passwordField.getPassword());
        Boolean checkBox = checkBoxField.isSelected();

        if (userName.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter a UserName!");
            userNameField.requestFocus();
        } else if (fullName.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter your Full Name!");
            fullNameField.requestFocus();
        } else if (!validateEmail(email)) {
            JOptionPane.showMessageDialog(null, "Enter a valid email ID!");
            emailField.requestFocus();
        } else if (password.length() <= 5) {
            JOptionPane.showMessageDialog(null, "Enter a password with atleast 6 characters!");
            passwordField.requestFocus();
        } else if (!checkBox) {
            JOptionPane.showMessageDialog(null, "You have to accept terms and conditions to continue!");
            checkBoxField.requestFocus();
        } else if (searchUserInfo("username", userName)) {
            JOptionPane.showMessageDialog(null, "Username already exists! Enter a different username.");
            userNameField.requestFocus();
        } else if (searchUserInfo("email", email)) {
            JOptionPane.showMessageDialog(null, "Email-ID has already been taken! Enter a different Email-ID.");
            emailField.requestFocus();
        } else {

            PreparedStatement preparedStatement;
            String insertionQuery = "INSERT INTO `logincredentials`.`userdetails`(`username`,`fullname`,`email`,`password`)VALUES(?,?,?,?);";

            try {
                preparedStatement = DatabaseConnection.getConnection().prepareStatement(insertionQuery);

                preparedStatement.setString(1, userName);
                preparedStatement.setString(2, fullName);
                preparedStatement.setString(3, email);
                preparedStatement.setString(4, password);

                if (preparedStatement.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "You have been registered!");
                    WelcomeFrame welcomePage = new WelcomeFrame();
                    welcomePage.setUserName(userName);
                    welcomePage.setVisible(true);
                    welcomePage.pack();
                    welcomePage.setLocationRelativeTo(null);
                    welcomePage.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(RegistrationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_signUpButtonMouseClicked

