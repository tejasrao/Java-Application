    /*
    * This method is used to validate email-ID using regular expressions
    * @parameter email - email-Id of type String
    * @return boolean This returns true if the email is valid else it returns false
    */
    private boolean validateEmail(String email) {
        Pattern pattern;
        Matcher matcher;
        String EMAIL_PATTERN = "^[a-zA-Z0-9_!#$%&'*+/=?`{|}~^.-]+@[a-zA-Z0-9.-]+\\.[A-Za-z]{2,6}$";
        pattern = Pattern.compile(EMAIL_PATTERN);
        matcher = pattern.matcher(email);
        return matcher.matches();
    }

    /*
    * This method is used to Search mySql database for the given username or email info
    * @parameter column - column name to be searched in the database
    * @parameter data - data to be searched in the corresponding column
    * @return boolean This returns true if the given data is exists else it returns false
    */
    private boolean searchUserInfo(String column, String data) {
        PreparedStatement preparedStmt;
        ResultSet resultSet;

        String query = "SELECT COUNT(*) FROM `logincredentials`.`userdetails` WHERE `" + column + "` = ?;";
        try {
            preparedStmt = DatabaseConnection.getConnection().prepareStatement(query);
            preparedStmt.setString(1, data);
            resultSet = preparedStmt.executeQuery();
            resultSet.next();
            if (resultSet.getInt(1) > 0) {
                return true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegistrationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
